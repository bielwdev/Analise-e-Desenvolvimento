var diaSem = 4;

switch(diaSem){
    case 1:
        console.log("Hoje é Domingo");
        break;
    case 2:
        console.log("Hoje é segunda-feira");
        break;
    case 3:
        console.log("Hoje é terça-feira");
        break;
    default:
        console.log("Esse dia da semana não existe")
}