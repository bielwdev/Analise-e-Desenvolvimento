public class Main {
    public static void main(String[] args) {

        int numero = 1;

        while(numero <=5){
            System.out.println(numero);
            numero++;

        }

        System.out.println("Fim do primeiro while");

        int numero2 = 9;

        do {
            System.out.println(numero2);
            numero2++;
        }while(numero2 <=5);

    }
}