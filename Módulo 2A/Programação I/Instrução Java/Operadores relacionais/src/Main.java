public class Main {
    public static void main(String[] args){
        int valor1, valor2;

        valor1 = 10;
        valor2 = 5;

        System.out.println(valor1 == valor2); // Igual
        System.out.println(valor1 != valor2); // Diferente
        System.out.println(valor1 >= valor2); // Maior ou igual
        System.out.println(valor1 <= valor2); // Menor ou igual
        System.out.println(valor1 > valor2); // Maior
        System.out.println(valor1 < valor2); // Menor
    }
}