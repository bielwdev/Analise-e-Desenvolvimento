public class Metodos {
    public static void main(String[] args) {

        String nome = "Gabriel Victor";
        System.out.println(nome);
        System.out.println(nome.length());
        System.out.println(nome.toLowerCase());
        System.out.println(nome.toUpperCase());
        System.out.println(nome.charAt(2));
        System.out.println(nome.isEmpty());
        System.out.println(nome.indexOf("br"));
        System.out.println(nome.contains("br"));
        System.out.println(nome.concat(" Santos"));
        System.out.println(nome.equals("gabriel victor"));
        System.out.println(nome.equalsIgnoreCase("gabriel victor"));
    }
}
